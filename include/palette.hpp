struct DmgPalette {
    const char* name;
    uint32_t colors[4];
};

inline constexpr uint32_t rgba(uint8_t r, uint8_t g, uint8_t b, uint8_t a = 0xFF) {
    return (static_cast<uint32_t>(a) << 24) |
           (static_cast<uint32_t>(b) << 16) |
           (static_cast<uint32_t>(g) << 8) |
           r;
}

